from abc import ABC, abstractmethod


class Stat(ABC):
    def __init__(self, iterable=None):
        if iterable is None:
            iterable = []
        self._num_list = iterable

    def add(self, num):
        self._num_list.append(num)

    def clear(self):
        self._num_list.clear()

    @abstractmethod
    def result(self):
        return NotImplemented


class MinStat(Stat):
    def result(self):
        if self._num_list:
            return min(self._num_list)
        else:
            return None


class MaxStat(Stat):
    def result(self):
        if self._num_list:
            return max(self._num_list)
        else:
            return None


class AverageStat(Stat):
    def result(self):
        return sum(self._num_list) / len(self._num_list) if self._num_list else None


